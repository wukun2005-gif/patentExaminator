para('[0073] 锂电池在充电过程中，电极/电解质界面处会发生电化学反应，产生电化学极化和浓差极化。电化学极化是由电荷转移反应的活化能垒引起的，浓差极化是由锂离子在电极材料中的扩散速率有限引起的。极化导致电池内阻增大，充电电压升高，充电效率降低，同时产生热量使电池温度升高。');
